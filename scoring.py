BREATHING_TYPE_SCORE_MAP = {t["key"]: t["score"] for t in BREATHING_TYPES}

RADAR_AXES = [
    {"label": "前屈系", "keys": ["maekutsu", "koukutsu"], "max": 8},
    {"label": "上肢系", "keys": ["banzai", "atede_gumi"], "max": 8},
    {"label": "体幹系", "keys": ["sokukutsu", "kubi_sokukutsu"], "max": 8},
    {"label": "回旋系", "keys": ["kyokaku_kubinashi", "kyokaku_kubiari", "kyokaku_kubiari_me"], "max": 12},
    {"label": "しゃがむ", "keys": ["shagamu"], "max": 4},
]


def calc_breathing_rate_score(count):
    if count is None:
        return 0
    for low, high, score in BREATHING_RATE_SCORE:
        if low <= count <= high:
            return score
    return 0


def calc_breathing_type_score(btype):
    return BREATHING_TYPE_SCORE_MAP.get(btype, 0)


def calc_scores(data):
    body_score = sum(
        int(data.get(m["key"], 0) or 0) for m in BODY_MOVEMENTS
    )
    breathing_rate_score = calc_breathing_rate_score(
        float(data["breathing_count"]) if data.get("breathing_count") else None
    )
    breathing_type_score = calc_breathing_type_score(data.get("breathing_type"))
    total = body_score + breathing_rate_score + breathing_type_score
    return {
        "body_score": body_score,
        "breathing_rate_score": breathing_rate_score,
        "breathing_type_score": breathing_type_score,
        "total_score": total,
    }


def calc_radar_values(eval_data):
    values = []
    for axis in RADAR_AXES:
        raw = sum(int(eval_data.get(k, 0) or 0) for k in axis["keys"])
        pct = round(raw / axis["max"] * 100)
        values.append(pct)
    return values
